package com.pm.model;

import java.util.Date;

public class StuExp {
	
	private int stuExpId;
	private int empId;
	private String stuExpName;
	private String stuExpType;
	private Date stuExpStartDate;
	private Date stuExpEndDate;
	private String stuExpAddress;
	private String stuExpMajor;
	private String stuExpGrade;
	private String stuExpNotes;
	
	
	public int getStuExpId() {
		return stuExpId;
	}
	public void setStuExpId(int stuExpId) {
		this.stuExpId = stuExpId;
	}
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getStuExpName() {
		return stuExpName;
	}
	public void setStuExpName(String stuExpName) {
		this.stuExpName = stuExpName;
	}
	public String getStuExpType() {
		return stuExpType;
	}
	public void setStuExpType(String stuExpType) {
		this.stuExpType = stuExpType;
	}
	public Date getStuExpStartDate() {
		return stuExpStartDate;
	}
	public void setStuExpStartDate(Date stuExpStartDate) {
		this.stuExpStartDate = stuExpStartDate;
	}
	public Date getStuExpEndDate() {
		return stuExpEndDate;
	}
	public void setStuExpEndDate(Date stuExpEndDate) {
		this.stuExpEndDate = stuExpEndDate;
	}
	public String getStuExpAddress() {
		return stuExpAddress;
	}
	public void setStuExpAddress(String stuExpAddress) {
		this.stuExpAddress = stuExpAddress;
	}
	public String getStuExpMajor() {
		return stuExpMajor;
	}
	public void setStuExpMajor(String stuExpMajor) {
		this.stuExpMajor = stuExpMajor;
	}
	public String getStuExpGrade() {
		return stuExpGrade;
	}
	public void setStuExpGrade(String stuExpGrade) {
		this.stuExpGrade = stuExpGrade;
	}
	public String getStuExpNotes() {
		return stuExpNotes;
	}
	public void setStuExpNotes(String stuExpNotes) {
		this.stuExpNotes = stuExpNotes;
	}
	
	
}
