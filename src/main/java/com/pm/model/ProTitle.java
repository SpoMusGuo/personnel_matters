package com.pm.model;

import java.util.Date;

public class ProTitle {

	private int proTitleId;
	private int empId;
	private String proTitleObtain;
	private String proTitleObtainWay;
	private Date proTitleObtainDate;
	private String proTitleNext;
	private Date proTitleNextTime;
	private String proTitleNotes;
	
	
	public int getProTitleId() {
		return proTitleId;
	}
	public void setProTitleId(int proTitleId) {
		this.proTitleId = proTitleId;
	}
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getProTitleObtain() {
		return proTitleObtain;
	}
	public void setProTitleObtain(String proTitleObtain) {
		this.proTitleObtain = proTitleObtain;
	}
	public String getProTitleObtainWay() {
		return proTitleObtainWay;
	}
	public void setProTitleObtainWay(String proTitleObtainWay) {
		this.proTitleObtainWay = proTitleObtainWay;
	}
	public Date getProTitleObtainDate() {
		return proTitleObtainDate;
	}
	public void setProTitleObtainDate(Date proTitleObtainDate) {
		this.proTitleObtainDate = proTitleObtainDate;
	}
	public String getProTitleNext() {
		return proTitleNext;
	}
	public void setProTitleNext(String proTitleNext) {
		this.proTitleNext = proTitleNext;
	}
	public Date getProTitleNextTime() {
		return proTitleNextTime;
	}
	public void setProTitleNextTime(Date proTitleNextTime) {
		this.proTitleNextTime = proTitleNextTime;
	}
	public String getProTitleNotes() {
		return proTitleNotes;
	}
	public void setProTitleNotes(String proTitleNotes) {
		this.proTitleNotes = proTitleNotes;
	}
	
}
